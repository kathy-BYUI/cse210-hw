class Reference
{
    //Attributes
    private string _reference;

    //Constructor
    public Reference()
    {
        _reference = "";
    }
	public Reference(string reference)
    {
        _reference = reference;
    }

    //Getters and setters
    public string GetReference()
    {
        return _reference;
    }
    public void SetReference(string reference)
    {
        _reference = reference;
    }

    //Methods
    
}